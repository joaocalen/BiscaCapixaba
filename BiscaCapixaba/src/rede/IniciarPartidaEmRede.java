    public static String obterEnderecoLocal() {
        String enderecoLocal = "";
        try {
            Enumeration e = NetworkInterface.getNetworkInterfaces();
            while (e.hasMoreElements()) {
                NetworkInterface n = (NetworkInterface) e.nextElement();
                Enumeration ee = n.getInetAddresses();
                while (ee.hasMoreElements()) {
                    InetAddress i = (InetAddress) ee.nextElement();
                    //System.out.println(i.getHostAddress());
                    if (i.isSiteLocalAddress()) {
                        enderecoLocal = i.getHostAddress();
                    }
                }
            }
        } catch (SocketException ex) {
            Logger.getLogger(IniciarPartidaEmRede.class.getName()).log(Level.SEVERE, null, ex);
        }
        return enderecoLocal;
    }

    public void abrirServidor() throws UnknownHostException, IOException {

        servidor = new Servidor();

        Thread t = new Thread(new Runnable() {
            @Override
            public void run() {
                servidor.receberPacote();
            }
        });
        t.start();

    }

